size_t __write(int handle, const unsigned char * buffer, size_t size)
{
  /* Sending in normal mode */
  if(HAL_OK == HAL_UART_Transmit(&huart1,(uint8_t *)buffer,size,100000))
  {
	return size;
  }
  else
  {
	return _LLIO_ERROR;
  }
}
